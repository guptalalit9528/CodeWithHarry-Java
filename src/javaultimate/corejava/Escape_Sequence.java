package corejava;

public class Escape_Sequence {
    public static void main(String[] args) {
        System.out.println("sdfgshdfuer\ngfysdhccvjyr");
        System.out.println("sdfgshdfuerg\tfysdhccvjyr");
        System.out.println("sdfgshdfuerg\\fysdhccvjyr");
        System.out.println("sdfgshdfuerg\'fysdhccvjyr");
        System.out.println("sdfgshdfuerg\bfysdhccvjyr");
    }
}
